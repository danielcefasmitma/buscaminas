    //Este metodo es para los botones de eliminar(botones peque침칩s)
    public BotonEliminar(JTable tabla, int row, String dir) {
        initComponents();
        
        //cuando se presione algun boton de eliminar peque침o se eliminar치 la fila pero tambien el archivo.
        btnEliminar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String nombreJugador = (String) tabla.getValueAt(row, 0); //Se obtiene el nombre del jugador en la tabla.
                File directorio = new File(dir);
                File[] files = directorio.listFiles();
                boolean hayArchivoJugador = false;
                String nombreRegistro = nombreJugador + ".txt";
                int i = 0;
                while (!hayArchivoJugador && i < files.length) {
                    hayArchivoJugador = nombreRegistro.equals(files[i].getName());
                    if (!hayArchivoJugador) {
                        i++;
                    }
                }
                files[i].delete();// se elimina el archiv txt.
                DefaultTableModel modeloTabla = (DefaultTableModel)tabla.getModel();
                modeloTabla.removeRow(row); //se elimina de la interfaz
            }

        });
    }

